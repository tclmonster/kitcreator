// gen-mimedb fetches the jshttp/mime-db database and generates a Go
// source file mapping file extensions to MIME types.
//
// Usage:
//
//	go run ./mime/bin/gen-mimedb > mime/buildsrc/mimedb.go
package main

import (
	"encoding/json"
	"fmt"
	"net/http"
	"os"
	"sort"
	"time"
)

const dbURL = "https://raw.githubusercontent.com/jshttp/mime-db/master/db.json"

type mimeEntry struct {
	Extensions []string `json:"extensions"`
}

func main() {
	client := &http.Client{Timeout: 30 * time.Second}
	resp, err := client.Get(dbURL)
	if err != nil {
		fmt.Fprintf(os.Stderr, "fetch: %v\n", err)
		os.Exit(1)
	}
	defer resp.Body.Close()
	if resp.StatusCode != 200 {
		fmt.Fprintf(os.Stderr, "fetch: HTTP %d\n", resp.StatusCode)
		os.Exit(1)
	}

	var db map[string]mimeEntry
	if err := json.NewDecoder(resp.Body).Decode(&db); err != nil {
		fmt.Fprintf(os.Stderr, "decode: %v\n", err)
		os.Exit(1)
	}

	extMap := make(map[string]string)
	for mimeType, entry := range db {
		for _, ext := range entry.Extensions {
			dotExt := "." + ext
			if _, exists := extMap[dotExt]; !exists {
				extMap[dotExt] = mimeType
			}
		}
	}

	exts := make([]string, 0, len(extMap))
	for ext := range extMap {
		exts = append(exts, ext)
	}
	sort.Strings(exts)

	fmt.Println("// Code generated by gen-mimedb; DO NOT EDIT.")
	fmt.Printf("// Source: %s\n", dbURL)
	fmt.Println("//")
	fmt.Println("// Data from mime-db, Copyright (c) 2014 Jonathan Ong <me@jongleberry.com>")
	fmt.Println("// Copyright (c) 2015-2022 Douglas Christopher Wilson <doug@somethingdoug.com>")
	fmt.Println("// Licensed under the MIT License.")
	fmt.Println()
	fmt.Println("package mime")
	fmt.Println()
	fmt.Println("var mimeDB = map[string]string{")
	for _, ext := range exts {
		fmt.Printf("\t%q: %q,\n", ext, extMap[ext])
	}
	fmt.Println("}")

	fmt.Fprintf(os.Stderr, "generated %d extension-to-type mappings\n", len(extMap))
}
